/**
 * 指定された章とタイプのパーソナライズドコンテンツを取得
 */
export function getPersonalizedContent(
  chapterId: string,
  primaryType: number
): {
  summary: string;
  characteristics: string[];
  practicalTips?: string;
} | null {
  const chapterContent = PERSONALIZED_LEARNING_CONTENT.find(
    (content) => content.chapterId === chapterId
  );

  if (!chapterContent) return null;

  return chapterContent.typeSpecific[primaryType] || null;
}
