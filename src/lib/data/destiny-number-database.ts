/**
 * データベースから運命数を取得
 */
export function getDestinyNumberFromDatabase(year: number, month: number): number {
  const entry = DESTINY_NUMBER_DATABASE.find(
    entry => entry.year === year && entry.month === month
  );
  
  if (entry) {
    return entry.destinyNumber;
  }
  
  throw new Error(`運命数データベースに${year}年${month}月のデータが存在しません。対応範囲: 1930-2030年`);
}

/**
 * データベースにデータが存在するかチェック
 */
export function hasDestinyNumberInDatabase(year: number, month: number): boolean {
  return DESTINY_NUMBER_DATABASE.some(
    entry => entry.year === year && entry.month === month
  );
}

/**
 * サポートされている年範囲を取得
 */
export function getSupportedYearRange(): { min: number; max: number } {
  return {
    min: 1930,
    max: 2030
  };
}

